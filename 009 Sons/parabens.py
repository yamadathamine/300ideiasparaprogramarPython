# Música é linear? - A respeito do programa do parabéns (1.4.2 ), 
# deseja-se saber se a melodia é preservada somando-se um valor constante a cada freqüência. 
# Faça um programa que lê essa constante (por exemplo, 100) e faz soar as notas somando a 
# constante a cada freqüência.

import pyaudio
import numpy as np

const = int(input("Digite a constante: "))

p = pyaudio.PyAudio()

volume = 0.5     # range [0.0, 1.0]
fs = 44100       # sampling rate, Hz, must be integer
duration = 0.2   # in seconds, may be float
f = 440 + const       # sine frequency, Hz, may be float

# generate samples, note conversion to float32 array
samples = (np.sin(2*np.pi*np.arange(fs*duration)*f/fs)).astype(np.float32)

# for paFloat32 sample values must be in range [-1.0, 1.0]
stream = p.open(format=pyaudio.paFloat32,
                channels=1,
                rate=fs,
                output=True)

# play. May repeat with different volume values (if done interactively) 
stream.write(volume*samples)

stream.stop_stream()
stream.close()

